            <h3>Pinnacle of the Temple</h3>
            <p>
                The pinnacle of the temple, where Jesus was tempted to throw himself down
                from, could've been next to the Kidron Valley on the eastern wall. This
                would've been a higher drop than even the south western corner shown in
                this picture. But many scholars believe it was more likely to be this south
                western corner where Jesus was tempted, because here the whole city could
                witness it, whereas hardly anyone would witness it in the Kidron Valley.
                Also, from here a priest would blow the trumpet to announce the beginning
                of Sabbath and other special events. So this corner became significant for
                the attention of the city's residents much like our media is to us today.
                And in the original Greek, "pinnacle" comes from a Greek word commonly used
                to refer to turret-like structures. Only later did its basic meaning change
                to the highest point.
            </p>
            <img src="img/pinnacle_of_temple.webp" alt="Picture of the Temple's Pinnacle"/>
        </article>

        <article class="bicolumn-right">
            <h3>Southern Steps</h3>
            <p>
                Jews walked up these steps when approaching the temple, especially during the
                great pilgrimage festivals of Passover, Shavuot and Sukkot. Christian
                archaeologists and scholars agree with 100% certainty that Jesus must've been
                here (see also a <a href="img/southern_steps_modern.webp">modern-day picture</a>
                of the restored southern steps). The temple itself was rebuilt in 1.5 years
                (back in 19 BC), and most of the temple mount was completed by 26 AD.
            </p>
            <img src="img/southern_steps.webp" alt="Picture of Southern Steps"/>
        </article>

        <article class="bicolumn-left">
            <h3>Antonia Fortress</h3>
            <p>
                The Antonia Fortress was adjacent to the temple mount so the Roman Soldiers
                who resided in the fortress could quickly descend upon the temple precincts
                if there was a riot. Also, it was slightly taller than the temple was, to let
                the Jews know "who's boss" and effectively "rub it in their faces." This was
                traditionally where Jesus was beaten and condemned to die, but more likely,
                <a href="http://biblehub.com/topical/p/praetorium.htm">because of Josephus's</a>
                (and Philo's) writings, he was beaten and condemned in what became known as
                Herod's Palace. If this is true (which seems most likely), it changes the
                validity of the "Via Dolorosa" as being where Jesus walked to his crucifixion.
            </p>
            <img src="img/antonia_fortress.webp" alt="Picture of the Antonia Fortress"/>
        </article>

        <article class="bicolumn-right">
            <h3>Model of Golgotha</h3>
            <p>
                The Church of the Holy Sepulchre was built on what is likely the site of
                Golgotha. In Jesus' time it was outside the city walls, but today it's within
                the old city walls because the old city today has its walls in a different
                place. There is no reason to believe that Jesus was crucified on top of a
                hill; he could've been crucified at the base of a hill. The Roman Catholic
                Church depicted the crucifixion at the top because it seems to exult Jesus
                better. But there is a good chance there's a hill there that looks like a
                skull, which is why the place was called "Golgotha." And this first-century
                replica of Jerusalem has such a hill on this site.
            </p>
