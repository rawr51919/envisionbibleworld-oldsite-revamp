        <article class="bicolumn-left">
            <h3>Northern Galilee</h3>
            <p>
                Galilee is a small, shallow, freshwater lake that gets stirred up very
                much and quickly, especially in January. A good contrast to compare is the
                ocean. Oceans require much more wind to change its ripples into waves,
                then it can take days afterward to calm down after a huge storm. A shallow
                lake, by contrast, develops waves much more unpredictably and suddenly,
                then can calm down to being mirror-like almost as soon as the wind stops.
                The ocean's waves also never obtain the same horizontal, narrow distance
                from their base to their crest, like a shallow lake's waves do. This means
                the sides of a shallow lake's waves are much steeper, which actually makes
                a shallow lake much more dangerous. Add to this the instability of the
                flat-bottomed boats the fishermen used in Jesus' day, compared to our
                modern-day boats with ballasts. And add to this the weather patterns of
                Galilee, which are due to the hills that surround the lake and the cool,
                prevailing westerly winds from the Mediterranean. The weather there is
                quite unpredictable, making Psalm 107:23-32 intensely real and personal
                to the fishermen of Jesus' day.
            </p>
            <img src="img/galilee_north.webp" alt="Picture of Northern Galilee"/>
        </article>

        <article class="bicolumn-right">
            <h3>Peter's House or "Insula"</h3>
            <p>
                This place in Capernaum is more than likely the location for Peter's house
                that existed in Jesus' day. Also, Peter's "insula" would've been built from
                the same basalt type of rock common to that area. But what remains there now
                was rebuilt, expanded, renovated, etc., from the way it was in Jesus' day,
                and archaeologists date these remains to an era of about 500 years after
                Jesus was here. Likely Peter's descendants and/or their friends continued to
                meet here in a house church after Peter died, and eventually it became more
                like a shrine, with pilgrims coming from afar to write their names on the
                walls, etc.
            </p>
            <img src="img/peters_house.webp" alt="Picture of Peter's House"/>
        </article>

