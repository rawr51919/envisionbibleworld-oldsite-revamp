            <h3>Insula</h3>
            <p>
                An “insula” is a compound where extended families lived in ancient 
		times. Grandparents typically lived with children, grandchildren  
		and possibly great-grandchildren. The size of an insula didn’t  
		indicate how rich the family was, because they could’ve just crammed 
		more people into that same insula. When a man was to get married,
		he would build a room onto the insula, then bring his wife to live
		with him and his parents, grandparents, etc. And since this was 
		what everyone did, Jesus used it to illustrate the home he has gone 
		to prepare for us, his bride, in John 14:2-3. “In my Father’s house
		are many rooms...and if I go and prepare a place for you, I will 
		come again and will take you to myself, that where I am you may be
		also.”
            </p>
            <img src="img/insula.jpg" alt="Picture of an Insula"/>
         </article>

