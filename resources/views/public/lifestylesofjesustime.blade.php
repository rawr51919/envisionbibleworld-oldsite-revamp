            <h3>Mikvahs</h3>
            <p>
                Mikvahs were much like mini pools except they were only used for 
		ritual cleansing. These mikvahs were on the south side of the 
		temple mount, where archaeological digging still reveals evidence 
		of them today. The Jews had to go into them for ritual cleansing 
		before going up into the temple. Outside were the beggars, believed 
		to be unclean because why else would they be born blind, maimed, 
		lame, deaf and mute, etc. (cp. John 9:2)? Beggars were perpetually 
		unclean because they were “born in sin,” so they would beg outside 
		the ritual cleansing places. There, outside the mikvahs, they could 
		obtain mercy from fellow Jews who might accidentally touch them 
		while giving their alms, but were about to be ritually cleansed
		anyway.
            </p>
            <img src="img/mikvahs.jpg" alt="Picture of temple mount wall with four entrances into mikvahs"/>
