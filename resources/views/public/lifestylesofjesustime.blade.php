            <h3>Caesarea Maritima’s Aqueduct</h3>
            <p>
                Caesarea Maritima was a secular, Roman city in Jesus’ day, and 
		had a hippodrome and “plumbing,” which was advanced technology  
		for that time. The city was built above a grid of tunnels that seems to have 
		been washed daily with the tide, keeping the city smelling cleaner than 
		almost every other Roman city at that time. Conscientious Jews 
		wouldn’t have anything to do with a city like this, if they could 
		help it. But the aqueduct in the below picture was 7 km long and 
		used during Jesus’ time to carry water from the north east.
            </p>
            <img src="img/caesarea_aqueduct.jpg" alt="Picture of Caesarea Maritima's aqueduct"/>
