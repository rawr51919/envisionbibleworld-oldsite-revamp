        <h1>Beliefs</h1>

        <article class="bicolumn-left">
            <ul>
                <li>
                    <h3>The Tri-une God. </h3>
                    <p>
                        I believe in one God, that he’s eternally existing in
                              three equally divine Persons: the Father, Son, and Holy
                              Spirit, who know, love, and glorify one another. This
                              living God is infinitely perfect in both love and holiness.
                              He is the Creator of all visible and invisible things, and
                              is worthy to receive all glory and adoration. Immortal and
                              eternal, he knows exhaustively the end from the beginning,
                              sustains and sovereignly rules over all things, and brings
                              about his eternal good purposes to redeem a people for
                              himself and restore his fallen creation, to the praise of
                              his glorious grace (Psalm 90:2; Proverbs 16:4; Isaiah 44:6;
                              Jeremiah 10:10; Matthew 3:17; Mark 12:29; Luke 9:35;
                              John 1:14, 4:24, 10:30, 15:26; 1 Corinthians 8:4-6;
                              Ephesians 1:11, 2:18; 1 Thessalonians 1:9; 1 Timothy 1:17;
                              Hebrews 1:1-3; 1 John 5:7; Revelation 4:8, 11).
                    </p>
                </li>
                <hr/>
                <li>
                    <h3>Creation. </h3>
                    <p>
                        I believe the Genesis account of creation is neither
                              allegory nor myth, but the literal, historical account of
                              the direct, immediate creative acts of God without any
                              evolutionary process. I believe that man was created by
                              a direct work of God and not from previously existing
                              forms of life, and that all men are descended from the
                              historical Adam and Eve, first parents of the entire human
                              race (Genesis 1 & 2; John 1:3; Colossians 1:16-17).
                    </p>
                </li>
                <hr/>
                <li>
                    <h3>The Plan of God. </h3>
                    <p>
                        I believe that from all eternity God determined in grace
                              to save a great multitude of guilty sinners from every
                              tribe, language, people and nation, and to this end foreknew
                              them and chose them. I believe God justifies and sanctifies
                              those who by grace have faith in Jesus, and he will one day
                              glorify them—all to the praise of his glorious grace. In
                              love, God commands and implores all people to repent and
                              believe; having set his saving love on those he has chosen,
                              and having ordained Christ to be their Redeemer (John 3:16;
                              Romans 8:30; Ephesians 1:11-14; 2 Timothy 1:9; Titus 3:4-5;
                              Revelation 5:9)
                    </p>
                </li>
                <hr/>
                <li>
                    <h3>The Gospel. </h3>
                    <p>
                        I believe the gospel is the good news of Jesus Christ, but
                              utter folly to the world. It is the power of God to those
                              who are being saved, but centers on the cross and resurrection
                              as the only means for salvation. It’s biblical (according to
                              the Scriptures), theological (Christ died for our sins to
                              reconcile us to God), historical (if the saving events did
                              not happen, our faith would be worthless), apostolic (the
                              message was entrusted to the apostles, witnesses of these
                              saving events), and intensely personal (for each individual
                              who receives, believes, and holds firmly to this truth).
                              Matthew 26:47-28:20; Mark 14:43-16:20; Luke 22:47-24:53;
                              John 18:1-25; 1 Corinthians 1:18, 23-24; Galatians 4:4-5;
                              Philippians 2:8-11; 1 Timothy 2:5-6; Hebrews 9:14;
                              1 Peter 3:18; 1 John 3:16.
                    </p>
                </li>
                <hr/>
                <li>
                    <h3>The Redemption of Christ. </h3>
                    <p>
                        I believe that the eternal Son was moved by love and
                              obedience to his Father to become human, and is referred
                              to as the Word which became flesh in John 1:1-13. He was
                              fully God and fully human being, the promised Messiah of
                              Israel, was conceived through the miraculous conception
                              of the Holy Spirit, and born of the virgin Mary. He perfectly
                              obeyed his heavenly Father, lived a sinless life, performed
                              miraculous signs, was crucified under Pontius Pilate, rose
                              from the dead on the third day, and ascended into heaven.
                              I believe that by his incarnation, life, death, resurrection
                              and ascension, Jesus Christ acted as our representative and
                              substitute. He did this so that in him I might become the
                              righteousness of God: on the cross he cancelled sin,
                              propitiated God, and, by bearing the full penalty of our
                              sins, reconciled to God all those who believe. By his
                                                resurrection Christ Jesus was vindicated by his Father,
                              broke the power of death and defeated Satan who once had
                              power over it, and brought everlasting life to all his
                              people. By his ascension he has been forever exalted as
                              Lord and has prepared a place for us to be with him. I
                              believe salvation is found in no one else, for there is
                              no other name given under heaven by which I must be saved.
                              Because God chose the lowly things of this world, the
                              despised things, the things that are not, to nullify the
                              things that are, no human being can ever boast before him.
                              Christ Jesus has become for us wisdom from God—that is,
                              our righteousness, holiness, and redemption (Isaiah 9:6;
                              Mark 10:45; John 1:1-3, 14; 20:27-29; Acts 20:28, 29:28;
                              Romans 3:23-25; 1 Corinthians 1:30, 6:20, 7:23;
                              Galatians 3:13-14, 4:4-7; Ephesians 1:7; Colossians 1:13-14;
                              1 Timothy 2:6; Titus 2:12b-14; Hebrews 9:12-15; 1 Peter
                              1:18-19; Revelation 5:9-10).
                    </p>
                </li>
                <hr/>
                <li>
                    <h3>Baptism and the Lord’s Supper. </h3>
                    <p>
                        I believe believer’s baptism and the Lord’s Supper are
                              ordained by the Lord Jesus himself. Baptism is connected
                              with entrance into the new covenant community; the Lord’s
                              Supper with ongoing covenant renewal. Together they are
                              simultaneously God’s pledge to us, divinely ordained means
                              of grace, our public vows of submission to the once
                              crucified and now resurrected Christ, and are anticipations
                              of his return and of the consummation of all things. They
                              are meant only for those who first trust in Christ’s
                              finished work on the cross as the only means to their
                              salvation, and are not meant to “make up for” any guilt
                              or sin (Matthew 3:11; 26:27-28; 28:19-20; Romans 6:3-6, 14;
                              7:18, 23-25; 1 Corinthians 6:11; 10:16, 21; 11:23-26;
                              12:13; 2 Corinthians 7:1; Galatians 3:27; 5:17, 24;
                              Ephesians 3:16-19; 1 Thessalonians 5:23-24; 2 Thessalonians
                              2:13; Titus 3:5; 1 Peter 2:11; 1 John 1:10)
                    </p>
                </li>
            </ul>
        </article>

        <article class="bicolumn-right">
            <ul>
                <li>
                    <h3>Revelation. </h3>
                    <p>
                        God has graciously disclosed his existence and power in
                              the created order, and has revealed himself to fallen
                              human beings in the person of his Son, the incarnate Word.
                              He is also a speaking God who by his Spirit has disclosed
                              himself in human words. I believe God inspired the words
                              in the Scriptures, preserved in the sixty-six books of the
                              Old and New Testament. These writings alone constitute the
                              verbally inspired Word of God, which are the record and
                              means of his saving work in the world, and are authoritative,
                              without error in the original writings. I believe they are
                              complete in their revelation of God’s will for salvation,
                              sufficient for all that God requires us to believe and do,
                              and final in its authority over every domain. I confess
                              both our human limitations and sinfulness prevent us from
                              knowing God’s truth exhaustively, but I believe that,
                              enlightened by the Spirit of God, I can know God’s truth
                              on all that matters most, for our eternal well-being and
                              for how to live lives pleasing to God. I believe the Bible
                              should be the center of true Christian unity and the supreme
                              standard by which all human conduct, creeds and opinions
                              should be tried (John 6:63, 10:25; Romans 1:19-22, 2:12-16;
                              2 Timothy 3:15-17; 2 Peter 1:19-21; 1 John 4:1).
                    </p>
                </li>
                <hr/>
                <li>
                    <h3>The Fall. </h3>
                    <p>
                        I believe Adam, made in the image of God, distorted that
                              image and lost his original blessings—for himself and all
                              his descendants—by falling into sin. As a result, all human
                              beings are alienated from God and corrupted in every aspect
                              of their being (e.g. physically, mentally, volitionally,
                              emotionally, spiritually). This means they’re all condemned
                              to eternal death apart from God’s own gracious intervention.
                              The supreme need of all human beings is to be reconciled to
                              the God under whose just and holy wrath I stand. The only
                              hope of all human beings is the undeserved love of this
                              same God, who alone can rescue us and restore us to himself.
                              And this means mankind is destined for the wrath of God
                              (Genesis 3:6-8, 6:5; Isaiah 64:6; Jeremiah 17:9; Matthew 25:41;
                              Romans 3:10-18 and 23, 5:12, 18-19; Ephesians 4:18;
                              2 Thessalonians 1:9; Titus 1:15)
                    </p>
                </li>
                <hr/>
                <li>
                    <h3>Death, Judgment and Hell. </h3>
                    <p>
                        I believe all have sinned, that our sins have stopped God
                              from hearing our prayers, and that I have been condemned
                              to eternal death and separation from God. The wages of sin
                              is death, it is appointed for man to die, and after that,
                              there’s judgement for all. Those who don’t repent will
                              spend eternity separated from God in the dreadful place
                              the Bible calls Hell (Isaiah 13:11; 59:2; Matthew 12:36-37;
                              25:31-34; Mark 9:42-49; Romans 2:5-6, 12; 3:23, 6:23;
                              1 Corinthians 6:9-10; Hebrews 9:27; Revelation 21:27).
                    </p>
                </li>
                <hr/>
                <li>
                    <h3>The Holy Spirit. </h3>
                    <p>
                        I believe this salvation is applied to his people by the
                              Holy Spirit. Sent by the Father and the Son, the Holy
                              Spirit glorifies the Lord Jesus Christ, is present with
                              and in believers. He convicts the world of sin, righteousness
                              and judgment, and by his powerful and mysterious work
                              regenerates spiritually dead sinners, awakening them to
                              repentance and faith. He “baptizes” them into union with
                              the Lord Jesus, so that they are justified before God by
                              grace, through faith. By the Spirit believers are renewed,
                              sanctified and adopted into God’s family; they participate
                              in the divine nature and receive his sovereignly distributed
                              gifts. The Holy Spirit is the down payment of the promised
                              inheritance, and indwells, guides, instructs, equips,
                              revives and empowers believers for Christ-like living and
                              service (Isaiah 11:1-2; John 3:5-8, 14:16-17, 26; 15:26;
                              16:7-14; 20:22; Acts 1:5; 2:1-4; Romans 8:2-6, 9-11, 14;
                              15:3; 1 Corinthians 2:10-13; 12:1-11; Ephesians 1:13-14,
                              4:30; 1 John 2:20).
                    </p>
                </li>
                <hr/>
                <li>
                    <h3>The Kingdom of God or of Heaven (used interchangeably in Matthew 19:23-24). </h3>
                    <p>
                        I believe those who have been saved by the grace of God
                              through union with Christ by faith, and through regeneration
                              by the Holy Spirit, enter the kingdom of God and delight in
                              the blessings of the new covenant. These blessings are the
                              forgiveness of sins, the inward transformation that awakens
                              a desire to glorify, trust and obey God, and the prospect
                              of the glory yet to be revealed. Good works constitute
                              indispensable evidence of saving grace. Living as salt in
                              a world that is decaying and light in a world that is dark,
                              believers should neither withdraw into seclusion from the
                              world, nor become indistinguishable from it. Recognizing
                              whose created order this is, and because I are citizens
                              of God’s kingdom, I are to love our neighbors as ourselves,
                              do good to all, and especially to those who belong to the
                              household of God. The kingdom of God, already inaugurated
                              but not fully realized, is the exercise of God’s sovereignty
                              in the world toward the eventual redemption of all creation.
                              The kingdom of God is an invasive power that plunders Satan’s
                              dark kingdom, and regenerates and renovates through repentance
                              and faith the lives of individuals rescued from that kingdom.
                              It therefore inevitably establishes a new community of human
                              life together under God (Matthew 3:2; 5:3, 10; 6:10; 13:24,
                              44-50; Mark 4:26-30; 14:25; Luke 19:11).
                    </p>
                </li>
