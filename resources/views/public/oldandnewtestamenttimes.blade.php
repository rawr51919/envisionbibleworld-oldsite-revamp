            <h3>Valley of Armageddon</h3>
            <p>
                This valley is beside Megiddo and receives it's name from Megiddo ("Har"
                means mount and "mageddon" refers to Megiddo). This valley is probably one
                of the main reasons civilizations kept coming back to live on top of the
                ghost towns of past civilizations. It's fertile, and Megiddo also has a
                source of water, which is another reason civilizations kept coming back.
                But since it was a desirable place, many felt it was worth fighting for,
                which is why the Valley of Armageddon was the scene of much blood shed
                throughout history. And that's why in Revelation 16:16, the prophecy refers
                to this valley as being the place for the final battle at the end of time.
                Whether or not this is literally the place, the "Valley of Armageddon" was
                an appropriate name for this prophecy.
            </p>
