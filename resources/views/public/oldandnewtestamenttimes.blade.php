            <h3>Grain Silo</h3>
            <p>
                This grain silo was also found at Megiddo, which dates back to King
                Jeroboam in the 8th century BC. Since Megiddo was already becoming a hill
                from numerous civilizations beforehand, and since hills were good to build
                fortresses on, it seems at least by Solomon's time that's exactly what
                Megiddo became. And this grain silo would've fed King Jeroboam's soldiers
                who also would've lived in this fortress. Fortresses stored the valuables
                of the nation (like grain and oil), and since they were naturally the last
                places to fall when there was war, that's where the soldiers lived and
                practiced their warfare maneuvers.
            </p>
