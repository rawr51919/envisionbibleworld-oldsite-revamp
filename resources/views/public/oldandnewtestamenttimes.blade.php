            <h3>Canaanite Civilization from Abraham's Time</h3>
            <p>
                These foundations were uncovered in the "tel" called Megiddo, which is a
                man-made hill. To understand a tel, think of a location where an ancient
                civilization lives for perhaps a few hundred years, then war, or a plague,
                or a bad fire, or a combination of sad events takes place, leaving the
                location as a ghost town, or perhaps as a bunch of mere foundations and
                dead bodies. After say, a hundred more years, enough dirt blows over to
                bury all of the foundations, but perhaps not all of the remaining
                structures. Then new peoples move in and appropriate some of what was left
                from before. But also, they go out to find more rocks to build on top.
                Then this cycle repeats again, over and over, which eventually creates a
                man-made hill.
            </p>
            <img src="img/canaanite_civilization.webp" alt="Picture of 2 holes in the ground like basements"/>
        </article>

