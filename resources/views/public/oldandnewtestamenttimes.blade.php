        <article class="bicolumn-right">
            <h3>Nomadic Tent</h3>
            <p>
                The promise of God in Exodus 3:8, that God would bring the
                Israelites to a land "flowing with milk and honey," speaks of two
                different lifestyles. In the north where there's more rain, the
                Israelites would farm, produce pollen, and the bees would produce
                honey. In the south where there's hardly any rain, there's hardy,
                sparse weeds. The Israelites would continue their shepherding
                heritage and nomadic lifestyles there, that they inherited from
                their parents wandering in the wilderness. Their goats would
                produce lots of milk, making life so different in the north from
                the south. This type of nomadic tent was also home to Abraham,
                Isaac and Jacob, who also lived as nomads.
            </p>
            <img src="img/nomadic_tent.webp" alt="Picture of a black goat-hair tent with nomad-type belongings"/>
